# Helper function to target and show specific art based on choice strings
def display_art(choice):
    if choice == "r":
        rock()
    elif choice == "p":
        paper()
    elif choice == "s":
        scissors()
    else:
        print("Invalid choice. Please choose 'r', 'p', or 's'.")

